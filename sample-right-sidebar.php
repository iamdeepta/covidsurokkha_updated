<div class="sample_content">
<h2>What is the COVID-19 virus?</h2>
<p>COVID-19 is a new strain of coronavirus that has not been previously identified in
humans. It was first identified in Wuhan, Hubei Province, China, where it has caused
a large and ongoing outbreak. It has since spread more widely in China. Cases have
since been identified in several other countries. The COVID-19 virus is closely
related to a bat coronavirus.</p>
<p>SARS-CoV-2 is closely related to the SARS-CoV. It is thought to have a zoonotic
origin. Genetic analysis has revealed that the coronavirus genetically clusters with
the genus Betacoronavirus, in subgenus Sarbecovirus (lineage B) together with two
bat-derived strains. It is 96% identical at the whole genome level to other bat
coronavirus samples (BatCov RaTG13). In February 2020, Chinese researchers found
that there is only one amino acid difference in certain parts of the genome
sequences between the viruses from pangolins and those from humans, however,
whole-genome comparison to date found at most 92% of genetic material shared between
pangolin coronavirus and SARS-CoV-2, which is insufficient to prove pangolins to be
the intermediate host.</p>
